    private function makeConfig(string $specFile = 'petstore.yaml'): GeneratorConfig
    {
        $config = new GeneratorConfig();
        $config->specFile = $specFile;
        $config->outputDir = $this->outputDir;
        $config->modelNamespace = 'Generated\\Model';
        $config->modelOutputDir = 'Model';
        $config->phpVersion = '8.2';

        return $config;
    }

    private const FIXTURES_DIR = __DIR__ . '/../Fixtures';

    private function removeDir(string $dir): void
    {
        if (!is_dir($dir)) {
            return;
        }
        $files = new \RecursiveIteratorIterator(
            new \RecursiveDirectoryIterator($dir, \FilesystemIterator::SKIP_DOTS),
            \RecursiveIteratorIterator::CHILD_FIRST
        );
        foreach ($files as $file) {
            $file->isDir() ? rmdir($file->getRealPath()) : unlink($file->getRealPath());
        }
        rmdir($dir);
    }

    private function extractMethodBody(string $content, string $methodName): string
    {
        $start = strpos($content, 'function ' . $methodName . '(');
        self::assertNotFalse($start, 'Method ' . $methodName . ' not found.');

        $next = strpos($content, "\n    public function ", $start + 1);
        if ($next === false) {
            $next = strpos($content, "\n    public static function ", $start + 1);
        }
        if ($next === false) {
            $next = strrpos($content, "\n}");
        }

        return substr($content, $start, $next - $start);
    }

    private function runPhpScript(string $script): string
    {
        $scriptFile = tempnam(sys_get_temp_dir(), 'openapi-gen-runtime-');
        self::assertNotFalse($scriptFile);

        file_put_contents($scriptFile, "<?php\n" . $script);

        try {
            $output = shell_exec(PHP_BINARY . ' ' . escapeshellarg($scriptFile) . ' 2>&1');
            self::assertNotFalse($output);

            return trim((string) $output);
        } finally {
            if (is_file($scriptFile)) {
                unlink($scriptFile);
            }
        }
    }
