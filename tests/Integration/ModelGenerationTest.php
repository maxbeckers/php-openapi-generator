    public function testReadOnlyWriteOnlyDirectionalArrayMethodsWorkAtRuntime(): void
    {
        $config = $this->makeConfig('read-write-flags.yaml');

        $service = self::$sharedService;
        $service->generate($config, self::FIXTURES_DIR);

        $script = <<<'PHP'
require __ACCOUNT_FILE__;

$request = \Generated\Model\Account::fromRequestArray([
    'id' => 123,
    'name' => 'Alice',
    'password' => 'secret123',
]);

$response = \Generated\Model\Account::fromResponseArray([
    'id' => 456,
    'name' => 'Bob',
    'password' => 'should-be-ignored',
]);

echo json_encode([
    'requestArray' => $request->toRequestArray(),
    'requestId' => $request->id,
    'responseArray' => $response->toResponseArray(),
    'responsePassword' => $response->password,
], JSON_THROW_ON_ERROR);
PHP;

        $script = str_replace('__ACCOUNT_FILE__', var_export($this->outputDir . '/Model/Account.php', true), $script);
        $output = $this->runPhpScript($script);

        $result = json_decode(trim($output), true, 512, JSON_THROW_ON_ERROR);

        self::assertArrayNotHasKey('id', $result['requestArray']);
        self::assertSame('Alice', $result['requestArray']['name']);
        self::assertSame('secret123', $result['requestArray']['password']);
        self::assertSame(0, $result['requestId']);
        self::assertSame(456, $result['responseArray']['id']);
        self::assertSame('Bob', $result['responseArray']['name']);
        self::assertArrayNotHasKey('password', $result['responseArray']);
        self::assertSame('', $result['responsePassword']);
    }

