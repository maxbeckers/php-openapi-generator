    private string $outputDir;
    private static OpenApiService $sharedService;

    public static function setUpBeforeClass(): void
    {
        self::$sharedService = new OpenApiService(new OpenApiLoader(), new FileWriter());
    }

    protected function setUp(): void
    {
        $this->outputDir = sys_get_temp_dir() . '/openapi-gen-test-' . uniqid('', true);
    }

    protected function tearDown(): void
    {
        $this->removeDir($this->outputDir);
    }

    public function testGeneratesModelsFromPetstore(): void
    {
        $config = $this->makeConfig();
        $config->classSuffix = '';
        $config->generateFromArray = true;
        $config->generateToArray = true;
        $config->phpReadonly = true;

        $service = self::$sharedService;
        $service->generate($config, self::FIXTURES_DIR);

        $modelDir = $this->outputDir . '/Model';

        self::assertDirectoryExists($modelDir);
        self::assertFileExists($modelDir . '/Pet.php');
        self::assertFileExists($modelDir . '/NewPet.php');
        self::assertFileExists($modelDir . '/PetStatus.php');
        // Pets is an array schema → Alias kind, no file generated
        self::assertFileDoesNotExist($modelDir . '/Pets.php');
    }

    public function testGeneratedPetClassHasCorrectStructure(): void
    {
        $config = $this->makeConfig();

