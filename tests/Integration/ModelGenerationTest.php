    public function testDirectionalValidationIgnoresReadOnlyInRequestsAndWriteOnlyInResponses(): void
    {
        $config = $this->makeConfig('read-write-flags.yaml');
        $config->validationStrategy = ValidationStrategy::NativeMethod;

        $service = self::$sharedService;
        $service->generate($config, self::FIXTURES_DIR);

        $content = file_get_contents($this->outputDir . '/Model/Account.php');

        self::assertStringContainsString('public function validateForRequest(): array', $content);
        self::assertStringContainsString('public function validateForResponse(): array', $content);
        self::assertStringNotContainsString("id must not be null.';", $this->extractMethodBody($content, 'validateForRequest'));
        self::assertStringNotContainsString("password must not be null.';", $this->extractMethodBody($content, 'validateForResponse'));

        $script = <<<'PHP'
require __ACCOUNT_FILE__;

$request = \Generated\Model\Account::fromRequestArray([
    'name' => 'Alice',
    'password' => 'secret123',
]);

$response = \Generated\Model\Account::fromResponseArray([
    'id' => 42,
    'name' => 'Alice',
]);

echo json_encode([
    'requestErrors' => $request->validateForRequest(),
    'responseErrors' => $response->validateForResponse(),
], JSON_THROW_ON_ERROR);
PHP;

        $script = str_replace('__ACCOUNT_FILE__', var_export($this->outputDir . '/Model/Account.php', true), $script);
        $output = $this->runPhpScript($script);

        $result = json_decode(trim((string) $output), true, 512, JSON_THROW_ON_ERROR);
        self::assertSame([], $result['requestErrors']);
        self::assertSame([], $result['responseErrors']);
    }

